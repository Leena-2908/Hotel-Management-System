    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int check=0;
       String email=jTextField1.getText();
       String password= jPasswordField1.getText();
       if(email.equals("")|| password.equals(""))
       {
           check=1;
           JOptionPane.showMessageDialog(null,"Every field is Required");
       }
       else if(email.equals("hms") && password.equals("admin"))
       {
           check=1;
           setVisible(false);
           new adminHome().setVisible(true);
       }
       else
       {
          ResultSet rs = Select.getData("select * from mytable where email ='"+ email +"' and password= '"+ password +"'");
          try
          {
              if(rs.next())
              {
                  check=1;
                  if(rs.getString(4).equals("true "))
                  {
                      setVisible(false);
                      new home().setVisible(true);
                  }
                  else
                  {
                      JOptionPane.showMessageDialog(null,"Wait for Admin Approval");
                  }
              }
          }
          catch(Exception e)
                  {
                  JOptionPane.showMessageDialog(null, e);
       }
        if(check==0)
            JOptionPane.showMessageDialog(null,"Incorrect E-mail or Password");
       }
     
    }//GEN-LAST:event_jButton1ActionPerformed

